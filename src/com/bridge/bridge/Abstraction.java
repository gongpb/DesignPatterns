package com.bridge.bridge;

public abstract class Abstraction {
	//定义对实现化角色的引用
	private Implementor imp;
	//约束子类必须实现 该构造方法
	public Abstraction(Implementor imp){
		this.imp = imp ;
	}
	//自身的行为和属性
	public void request(){
		this.imp.doSomething();
	}
	//获得实现化角色
	public Implementor getImp() {
		return imp;
	}
	
}
